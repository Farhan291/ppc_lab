 //C-Program to calculate the sum of three numbers
// C Program to Find Sum of 3 Numbers
#include <stdio.h>
int main(){
 int a, b, c, sum;
 
 // Asking for input
 printf("Enter 3 numbers: \n");
 scanf("%d %d %d", &a, &b, &c);
 // Calculating sum
 sum = a + b + c;
 // Displaying output
 printf("Sum = %d \n", sum);
 return 0;}
